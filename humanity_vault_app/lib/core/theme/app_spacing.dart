/// Centralized spacing tokens for the Humanity Vault design system,
/// following an 8pt grid.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 16;
  static const double lg = 24;
  static const double xl = 32;

  /// Standard corner radius for cards and tiles.
  static const double cardRadius = 16;

  /// Corner radius for hero banners.
  static const double heroRadius = 20;

  /// Standard screen content padding.
  static const double screenPadding = 16;
}
